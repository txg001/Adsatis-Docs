<h2 id="step-1-create-a-new-repository" tabindex="-1"><a class="header-anchor" href="#step-1-create-a-new-repository" aria-hidden="true">#</a> Step 1: Create a new repository</h2>
<p>When you create a new repository on Bitbucket Cloud it is always created empty (e.g. without any code in it). To create a new repo:</p>
<ol>
<li>
<p>From <a href="">BitBucket</a> click the + icon in the global sidebar and select <strong>Repository</strong>.</p>
<p>Bitbucket will display the &quot;Create a new repository&quot; dialog box.</p>
</li>
<li>
<p>Enter <code>HelloWorld</code> as the &quot;repository name&quot;. This will form part of the Git repository URL, so it is important not to use spaces.</p>
</li>
</ol>
<p>You can leave the rest of the options as they are, and then click &quot;Create repository&quot;.</p>
